// overrides system _write so we can use SWD
int _write(int file, char* ptr, int len)
{
	for (int DataIdx = 0; DataIdx < len; DataIdx++)
	{
		ITM_SendChar(*ptr++);
	}
	return len;
}

void hexprint(const char* start, const uint8_t* buf, int len)
{
	printf("%s0x", start);
	for (int i = 0; i < len; i++)
	{
		printf("%.2hX", buf[i]);
	}
	printf("\n");
}


